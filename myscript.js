const form = document.getElementById('form');
const firstname = document.getElementById('firstname');
const lastname = document.getElementById('lastname');
const email = document.getElementById('email');
const container = document.querySelector('.container');
const animateCircles = document.querySelector('.animate-circles');

form.addEventListener('submit', (e) => { 
    e.preventDefault();

    const firstnameValue = firstname.value;
    const lastnameValue = lastname.value;
    const emailValue = email.value;

    if (firstnameValue === '') {
        errorMessage(firstname, "Firstname is empty");
    } else {
        successMessage(firstname);
    }

    if (lastnameValue === '') {
        errorMessage(lastname, "Lastname is empty");
    } else {
        successMessage(lastname);
    }

    if (emailValue === '') {
        errorMessage(email, "Email is empty");
    } else if (!validateEmail(emailValue)) {
        errorMessage(email, "Email is invalid");
    } else {
        successMessage(email);
    }


    if (firstname.parentElement.classList.contains('success') && lastname.parentElement.classList.contains('success') && email.parentElement.classList.contains('success')) {

        container.classList.add('complete');
        animateCircles.classList.add('complete');

    }
});


